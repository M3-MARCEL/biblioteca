class DetalleLibro:
    def __init__(self, categoria, genero, numero_paginas, editorial, formato):
        self.categoria = categoria  # Atributo privado: categoría del libro (Encapsulamiento)
        self.genero = genero  # Atributo privado: género del libro (Encapsulamiento)
        self.numero_paginas = numero_paginas  # Atributo privado: número de páginas (Encapsulamiento)
        self.editorial = editorial  # Atributo privado: editorial del libro (Encapsulamiento)
        self.formato = formato  # Atributo privado: formato del libro (Encapsulamiento)